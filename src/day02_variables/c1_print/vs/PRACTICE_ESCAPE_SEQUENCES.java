package day02_variables.c1_print.vs;

public class PRACTICE_ESCAPE_SEQUENCES {

    //  //    escape sequences:
    ////   \t: paragraph space
    ////  \n: new line
    ////  \": prints double quote " on the console
    ////  \\: prints \ on the console
    ////  \': prints single quote
    ////   ' : prints single quote
    public static void main(String[] args) {
        ////   \t: paragraph space

        System.out.println("\tt makes space on paragraph");
        System.out.println("\t for example \tthis \t\t\t\t\t\tis");

        ////  \n: new line
        System.out.println("hello world \nthis is \tgonna be at the new line");


        ////  \\: prints \ on the console,

        System.out.println("\\");


        ////  \": prints double quote " on the console
        System.out.println("\"");
        System.out.println("if you want double quote you need to use like this \" meraba\" " );















    }

















}
